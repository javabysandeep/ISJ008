package operators;

public class BitWiseOperator2 {
    public static void main(String[] args) {
        System.out.println(4 & 5); //4
        System.out.println(14 & 15); //14
        System.out.println(34 & 5); //0
        System.out.println(41 & 3); //1

        System.out.println("Bitwise OR");
        System.out.println(4 | 5); //5
        System.out.println(14 | 15); //15
        System.out.println(34 | 5); //39
        System.out.println(41 | 3); //43


    }
}
