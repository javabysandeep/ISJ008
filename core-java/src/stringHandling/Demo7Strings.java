package stringHandling;

public class Demo7Strings {
    public static void main(String[] args) {
        char[] strings = {'G', 'o', 'o', 'd', ' ', 'm', 'o', 'r', 'n', 'i', 'n', 'g'};
        for (char ch : strings) {
            System.out.print(ch);
        }
        String str = "Good morning";
        System.out.println(str);
    }
}
